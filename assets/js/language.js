  function setActiveLang(lang) {
    const idBtn = document.getElementById("idBtn");
    const engBtn = document.getElementById("engBtn");

    if (!idBtn || !engBtn) return; // kalau tombol belum ada, keluar dulu

    if (lang === "id") {
      idBtn.classList.add("active");
      engBtn.classList.remove("active");
    } else {
      engBtn.classList.add("active");
      idBtn.classList.remove("active");
    }
  }
  let currentLang = "eng"; // default pertama
  function changeLanguage(lang) {
    currentLang = lang; // ✅ update bahasa global
    
    document.querySelectorAll("[data-i18n-key]").forEach(el => {
      const key = el.getAttribute("data-i18n-key");
      if (translations[lang][key]) {
        el.innerHTML = translations[lang][key];
      }
  });

  setActiveLang(lang);

  // ✅ kalau ada fungsi update deskripsi terakhir dipilih (scale)
  if (window.updateDescBox) {
    window.updateDescBox();
  }
}

window.addEventListener("load", () => changeLanguage(currentLang));
